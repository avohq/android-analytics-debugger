package app.avo.androidanalyticsdebugger.model;

public class DebuggerProp {

    public String id;
    public String name;
    public String value;

    public DebuggerProp() {}

    public DebuggerProp(String id, String name, String value) {
        this.id = id;
        this.name = name;
        this.value = value;
    }
}